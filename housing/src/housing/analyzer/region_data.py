# 주소 문자열에서 시/군/구명을 추출하여 법정동코드로 변환
def address_to_lawd_cd(address: str) -> str:
    """주소 문자열에서 시/군/구를 파악하여 5자리 법정동코드를 반환합니다.

    Args:
        address: "서울특별시 성북구 장위동 68-37" 형태의 주소

    Returns:
        5자리 법정동코드 문자열, 또는 빈 문자열(매칭 실패)
    """
    if not address:
        return ""

    # 가장 긴 키부터 매칭 (시/군/구 단위 우선)
    # 예: "경기도 수원시 영통구"가 "경기도 수원시"보다 먼저 매칭되어야 함
    keys = sorted(SIGUNGU_TO_LAWD.keys(), key=len, reverse=True)
    for key in keys:
        if key in address:
            return SIGUNGU_TO_LAWD[key]

    # 두번째 시도: address 첫 부분이 "서울특별시" 같은 시/도명으로 시작하면
    # 해당 시/도 전체를 대표하는 코드 사용
    for key in SIGUNGU_TO_LAWD:
        if address.startswith(key):
            return SIGUNGU_TO_LAWD[key]

    return ""
