def get_region_score(
    region_name: str,
    overrides: Optional[dict[str, float]] = None,
) -> float:
    """지역명으로 교통/입지 점수를 조회합니다.

    Args:
        region_name: 시/도 또는 시/군/구명 ("서울특별시 강남구" 등)
        overrides: 개별 오버라이드

    Returns:
        0-100 사이 점수
    """
    if overrides and region_name in overrides:
        return overrides[region_name]

    # 정확히 일치
    if region_name in REGION_SCORES:
        return REGION_SCORES[region_name]

    # "서울특별시 강남구" → "서울특별시" + "강남구"
    for sep in [" ", "  "]:
        parts = region_name.split(sep, 1)
        if len(parts) == 2:
            # 시/도 단위 점수
            main_region = parts[0]
            sub_region = parts[1]
            base = REGION_SCORES.get(main_region, 50.0)

            # 구 단위 가산점 (서울 지하철 역세권)
            bonus = 0.0
            if sub_region in SUBWAY_DISTRICTS:
                bonus = 10.0

            return min(base + bonus, 100.0)

    # 부분 매칭
    for key, score in REGION_SCORES.items():
        if key in region_name or region_name in key:
            return score

    return 50.0


def get_gtx_bonus(station_name: str) -> float:
    """GTX 역세권 가산점을 반환합니다."""
    return GTX_STATIONS.get(station_name, 0.0)
